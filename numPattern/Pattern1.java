package numPattern;

public class Pattern1 {

	public static void main(String[] args)
	{
	System.out.println("Program Started");
	int line=5;
	int numcount=5;
	int num=1;
	for(int i=1;i<=line;i++)
	{
		for(int j=1;j<=numcount;j++)
		{
		System.out.print(num);	
		}
		System.out.println();
	}
	System.out.println("Program Ended");
	}
}
